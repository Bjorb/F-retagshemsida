function openNav() {
  document.getElementById('frontPage').style.height = '17vh'
   document.getElementById('text').style.top = '-60vh'
   document.getElementById('close').style.top = '-35vh'
}


function closeNav() {
  document.getElementById('frontPage').style.height = '100vh'
  document.getElementById('text').style.top = '15vh'
   document.getElementById('close').style.top = '42vh'
}